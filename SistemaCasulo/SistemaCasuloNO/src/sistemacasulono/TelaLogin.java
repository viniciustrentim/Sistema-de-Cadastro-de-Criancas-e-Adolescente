    private void btnLogarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLogarActionPerformed
        try {
            Connection con;
            con = (Connection) DriverManager.getConnection("jdbc:mysql://127.0.0.1/sistemacasulo", "root", "");
            String sql = "Select*from telalogin Where Login=? and Senha=?";
            PreparedStatement ps = con.prepareStatement(sql);
            ps.setString(1, txtLogin.getText());
            ps.setString(2, pfSenha.getText());
            ResultSet rs;
            rs = ps.executeQuery();
            if (rs.next()) {
                Menu mn = new Menu();
                mn.setLocation(400, 100);
                mn.setVisible(true);
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(null, "Usu√°rio ou senha Incorretos", "Alerta", JOptionPane.WARNING_MESSAGE);
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "SQLException: " + ex.getMessage() + "\nSQLState: " + ex.getSQLState()
                    + "\nVendorError: " + ex.getErrorCode());
        }
        getRootPane().setDefaultButton(btnLogar);

    }//GEN-LAST:event_btnLogarActionPerformed

