    private void btnRegistrarAdocaoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarAdocaoActionPerformed

        int linha = tblDados.getSelectedRow();

        String valor = String.valueOf(tblDados.getValueAt(linha, 0).toString());
        //JOptionPane.showMessageDialog(null, "Criança" + valor);
        try {

            Connection con;
            con = (Connection) DriverManager.getConnection("jdbc:mysql://127.0.0.1/sistemacasulo", "root", "");
            String sql;// String para receber a Scrip(O comando SQL que será usado-Insert/Update/DLETE)
            sql = "update info_crianca_adolescente set situacao = 'adotado' where ID_Crianca_Adoles = ?";
            PreparedStatement ps;
            ps = con.prepareStatement(sql);
            ps.setString(1, valor);
            int res = ps.executeUpdate();
            if (res > 0) {
                JOptionPane.showMessageDialog(null, "Adoção feita com sucesso");
            } else {
                JOptionPane.showMessageDialog(null, "Falha na alteração!!");
            }
        } catch (SQLException ex) {
            java.util.logging.Logger.getLogger(Pesquisa.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnRegistrarAdocaoActionPerformed

