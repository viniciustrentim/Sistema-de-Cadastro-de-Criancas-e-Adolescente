    private void btnPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarActionPerformed
        try {
            Connection con;
            con = DriverManager.getConnection("jdbc:mysql://127.0.0.1/sistemacasulo", "root", "");
            String sql = null;
            sql = "SELECT * FROM info_crianca_adolescente WHERE Nome_Comp LIKE ?";
            PreparedStatement ps = con.prepareStatement(sql);

            ps.setString(1, "%" + txtNomeCriAd.getText() + "%");
            ResultSet rs = ps.executeQuery();
            DefaultTableModel m = (DefaultTableModel) tblDados.getModel();
            //limpar o jTable
            while (tblDados.getRowCount() > 0) {
                m.removeRow(0); //apaga a primeira linha
            }
            //transferir o conteúdo do rs para o jTable1
            while (rs.next()) {//enquanto existirem registros
                Object linha[] = new Object[13]; //vetor com 13 elementos
                linha[0] = rs.getInt("ID_Crianca_Adoles");
                linha[1] = rs.getBoolean("Sem_Registro");//pegando cada um dos campos
                linha[2] = rs.getString("Nome_Comp");
                linha[3] = rs.getString("Como_Chamar");
                linha[4] = rs.getString("Sexo");
                linha[5] = rs.getString("Cor_raca");
                linha[6] = rs.getString("Nacionalidade");
                linha[7] = rs.getString("Naturalidade");
                linha[8] = rs.getString("Data_nasc");
                linha[9] = rs.getString("Idade");
                linha[10] = rs.getBoolean("Idade_presu");
                linha[11] = rs.getString("situacao");
                m.addRow(linha);//adicionado o vetor ao jTable1
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "SQLException" + ex.getMessage() + "\nSQLState" + ex.getSQLState() + "\nVendorError");

        }
    }//GEN-LAST:event_btnPesquisarActionPerformed

