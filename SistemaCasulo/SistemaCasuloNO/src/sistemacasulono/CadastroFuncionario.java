    private void btnPesquisaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisaActionPerformed
        try {
            Connection con;
            con = DriverManager.getConnection("jdbc:mysql://127.0.0.1/sistemacasulo", "root", "");
            String sql = null;
            sql = "SELECT * FROM telalogin WHERE ID_Funcionario LIKE ?";
            PreparedStatement ps = con.prepareStatement(sql);

            ps.setString(1, "%" + txtID.getText() + "%");
            ResultSet rs = ps.executeQuery();
            if (rs.next()) {
                txtLogin.setText(rs.getString("Login"));
                txtNome.setText(rs.getString("Nome_do_Funcionario"));
                pfSenha.setText(rs.getString("Senha"));
            } else {
                JOptionPane.showMessageDialog(null, "Registro não encontrado", "Mensagem", JOptionPane.WARNING_MESSAGE);
            }
       
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "SQLException" + ex.getMessage() + "\nSQLState" + ex.getSQLState() + "\nVendorError");

        }
    }//GEN-LAST:event_btnPesquisaActionPerformed

