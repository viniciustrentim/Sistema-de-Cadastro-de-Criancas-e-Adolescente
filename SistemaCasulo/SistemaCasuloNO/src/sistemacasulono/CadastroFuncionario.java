    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        try {

            Connection con;//Objeto para criar uma ligação com o banco de dados
            con = (Connection) DriverManager.getConnection("jdbc:mysql://127.0.0.1/sistemacasulo", "root", "");
            String sql;// String para receber a Scrip(O comando SQL que será usado-Insert/Update/DLETE)
            sql = "UPDATE telalogin set Nome_do_Funcionario=?,Login=?,Senha=? where ID_Funcionario=?";//Script para alterar dados no BD
            /*uma forma de você fazer uma inserção no banco mais segura,
             onde você prepara os parametros para serem inseridos */
            PreparedStatement ps;
            ps = (PreparedStatement) con.prepareStatement(sql);
            ps.setString(1, txtNome.getText());
            ps.setString(2, txtLogin.getText());
            ps.setString(3, pfSenha.getText());
            ps.setString(4, txtID.getText());

            int res = ps.executeUpdate();//executa o comando e retorna a qtd de registros incluidos
            if (res > 0) {//se for inserido um registro res>0!, deu certo.
                JOptionPane.showMessageDialog(null, "Alterado com sucesso");
            } else {//se nao deu certo
                JOptionPane.showMessageDialog(null, "Nenhum registro alterado");
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "SQLException: " + ex.getMessage() + "\nSQLState: " + ex.getSQLState() + "\nVendedorError: " + ex.getErrorCode());
        }

    }

